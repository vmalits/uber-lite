        <h1 id="auth">Auth</h1>

    <p>Request OTP Code</p>
<p>This endpoint generates a one-time password (OTP) for the given phone number
and sends it via SMS.</p>
<p>Rate limited to 3 requests per 15 minutes per phone number.</p>

